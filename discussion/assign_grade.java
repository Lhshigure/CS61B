public class assign_grade {
    public static void grade(int grade){
        if(grade < 60){
            System.out.println("F");
        }
        else if(grade < 70){
            System.out.println("F");
        }
        else if(grade < 80){
            System.out.println("F");
        }
        else if(grade < 90){
            System.out.println("F");
        }else{
            System.out.println("A");
        }
    }
}
